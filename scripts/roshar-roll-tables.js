function makeResults(entries) {
  return entries.map((text, index) => ({
    text,
    weight: 1,
    range: [index + 1, index + 1],
  }));
}

export function buildRoadmapRollTableDocuments({ folderId } = {}) {
  return ROADMAP_ROLL_TABLE_GROUPS.flatMap(group => group.tables.map(table => ({
    name: table.name,
    formula: "1d20",
    replacement: true,
    displayRoll: true,
    folder: folderId,
    results: makeResults(table.entries),
  })));
}

export async function ensureRoadmapRollTables({
  parentFolder,
  game = globalThis.game,
  Folder = globalThis.Folder,
  RollTable = globalThis.RollTable,
  ui = globalThis.ui,
} = {}) {
  const group = ROADMAP_ROLL_TABLE_GROUPS[0];
  let folder = game?.folders?.find?.(item =>
    item.name === group.folderName &&
    item.type === "RollTable" &&
    (!parentFolder || item.folder?.id === parentFolder.id)
  );

  if (!folder) {
    folder = await Folder.create({
      name: group.folderName,
      type: "RollTable",
      color: group.color,
      folder: parentFolder?.id,
      sorting: "a",
    });
  }

  let created = 0;
  let reorganized = 0;
  for (const tableData of buildRoadmapRollTableDocuments({ folderId: folder.id })) {
    const existing = game.tables.getName(tableData.name);
    if (existing && existing.folder?.id !== folder.id) {
      await existing.delete();
      reorganized += 1;
    }
    if (!game.tables.getName(tableData.name)) {
      await RollTable.create(tableData);
      created += 1;
    }
  }

  if (created || reorganized) {
    ui?.notifications?.info?.(`Cosmere RPG Tooling: ${created} tabla(s) tematicas creadas, ${reorganized} reorganizada(s).`);
  }

  return { folder, created, reorganized, total: group.tables.length };
}
